# Control flow in Python

# * ~~ Conditional Statements ~~

# if
age = 20
if age >= 18:
    print("You're an adult")
    
    
# if - else
if age >= 18:
    print("You're an adult")
else:
    print("You're a minor")
    
    
# if - elif - else
score = 85
if score >= 90:
    print("A")
elif score >= 80:
    print("B")
else:
    print("C")
    

